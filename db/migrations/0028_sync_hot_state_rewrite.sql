-- Ordering note: migration filenames execute lexicographically, so db/migrations/0028_created_at_ordering_indexes.sql runs before db/migrations/0028_sync_hot_state_rewrite.sql.
-- See also: docs/architecture.md.
-- Replace the legacy global replay log with separate hot-state and review
-- history sync lanes. Current-state bootstrap now reads canonical tables
-- directly, while incremental pull reads a compact metadata-only hot log.

-- Review history now advances on its own append-only cursor instead of
-- piggy-backing on the mutable hot-state feed.
ALTER TABLE content.review_events
  ADD COLUMN IF NOT EXISTS review_sequence BIGINT GENERATED BY DEFAULT AS IDENTITY;

CREATE INDEX IF NOT EXISTS idx_review_events_workspace_review_sequence
  ON content.review_events(workspace_id, review_sequence);

COMMENT ON COLUMN content.review_events.review_sequence IS 'Monotonic append-only review-history cursor used by sync/review-history/pull.';

-- Workspace-level sync metadata keeps retention and bootstrap state explicit.
CREATE TABLE IF NOT EXISTS sync.workspace_sync_metadata (
  workspace_id UUID PRIMARY KEY REFERENCES org.workspaces(workspace_id) ON DELETE CASCADE,
  min_available_hot_change_id BIGINT NOT NULL DEFAULT 0,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

COMMENT ON TABLE sync.workspace_sync_metadata IS 'Workspace-scoped sync metadata such as the oldest hot cursor still available for incremental pull.';
COMMENT ON COLUMN sync.workspace_sync_metadata.min_available_hot_change_id IS 'Oldest hot cursor still available for this workspace. Older clients must re-bootstrap.';

-- Compact mutable-root hot log. It keeps only identity and LWW metadata, not
-- full JSON snapshots or append-only review history.
CREATE TABLE IF NOT EXISTS sync.hot_changes (
  change_id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  workspace_id UUID NOT NULL REFERENCES org.workspaces(workspace_id) ON DELETE CASCADE,
  entity_type TEXT NOT NULL CHECK (entity_type IN ('card', 'deck', 'workspace_scheduler_settings')),
  entity_id TEXT NOT NULL,
  action TEXT NOT NULL CHECK (action IN ('upsert')),
  device_id UUID NOT NULL REFERENCES sync.devices(device_id) ON DELETE RESTRICT,
  operation_id TEXT NOT NULL,
  client_updated_at TIMESTAMPTZ NOT NULL,
  recorded_at TIMESTAMPTZ NOT NULL DEFAULT now()
) PARTITION BY RANGE (recorded_at);

CREATE TABLE IF NOT EXISTS sync.hot_changes_2026
  PARTITION OF sync.hot_changes
  FOR VALUES FROM ('2026-01-01 00:00:00+00') TO ('2027-01-01 00:00:00+00');

CREATE TABLE IF NOT EXISTS sync.hot_changes_2027
  PARTITION OF sync.hot_changes
  FOR VALUES FROM ('2027-01-01 00:00:00+00') TO ('2028-01-01 00:00:00+00');

CREATE TABLE IF NOT EXISTS sync.hot_changes_default
  PARTITION OF sync.hot_changes DEFAULT;

CREATE INDEX IF NOT EXISTS idx_hot_changes_workspace_change_id
  ON sync.hot_changes(workspace_id, change_id);

CREATE INDEX IF NOT EXISTS idx_hot_changes_workspace_entity_latest
  ON sync.hot_changes(workspace_id, entity_type, entity_id, change_id DESC);

COMMENT ON TABLE sync.hot_changes IS 'Compact hot-state mutation log for mutable sync roots only. Bootstrap reads canonical tables directly.';
COMMENT ON COLUMN sync.hot_changes.client_updated_at IS 'Client-provided LWW timestamp of the winning mutable mutation.';

-- Bounded idempotency ledger for batched push requests.
CREATE TABLE IF NOT EXISTS sync.applied_operations_current (
  workspace_id UUID NOT NULL REFERENCES org.workspaces(workspace_id) ON DELETE CASCADE,
  device_id UUID NOT NULL REFERENCES sync.devices(device_id) ON DELETE RESTRICT,
  operation_id TEXT NOT NULL,
  operation_type TEXT NOT NULL,
  entity_type TEXT NOT NULL CHECK (entity_type IN ('card', 'deck', 'workspace_scheduler_settings', 'review_event')),
  entity_id TEXT NOT NULL,
  client_updated_at TIMESTAMPTZ NOT NULL,
  resulting_hot_change_id BIGINT,
  applied_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  PRIMARY KEY (workspace_id, device_id, operation_id, applied_at)
) PARTITION BY RANGE (applied_at);

CREATE TABLE IF NOT EXISTS sync.applied_operations_current_2026
  PARTITION OF sync.applied_operations_current
  FOR VALUES FROM ('2026-01-01 00:00:00+00') TO ('2027-01-01 00:00:00+00');

CREATE TABLE IF NOT EXISTS sync.applied_operations_current_2027
