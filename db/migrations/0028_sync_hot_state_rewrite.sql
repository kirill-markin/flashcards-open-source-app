CREATE TABLE IF NOT EXISTS sync.applied_operations_current_default
  PARTITION OF sync.applied_operations_current DEFAULT;

CREATE INDEX IF NOT EXISTS idx_applied_operations_current_workspace_device_operation
  ON sync.applied_operations_current(workspace_id, device_id, operation_id, applied_at DESC);

COMMENT ON TABLE sync.applied_operations_current IS 'Bounded idempotency ledger for sync/push batches after the hot-state rewrite.';

